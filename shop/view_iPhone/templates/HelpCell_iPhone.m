#import "model.h"

#pragma mark -

@implementation HelpCell_iPhone

DEF_SIGNAL( TOUCHED )

SUPPORT_RESOURCE_LOADING( YES )

- (void)dataDidChanged
{
    self.tapEnabled = YES;
    self.tapSignal = self.TOUCHED;
    
	ARTICLE_GROUP * articleGroup = self.data;
	if ( articleGroup )
	{
		$(@"#helper-title").TEXT( articleGroup.name );
		$(@"#helper-subtitle").TEXT( nil );
	}
}

